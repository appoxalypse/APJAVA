public class Test09 {

  public static void main(String[] args) {

    int temp = 100;

    if (temp < 30) {
      System.out.println("It is freezing");
    } else if (temp < 50) {
      System.out.println("It is cold");
    } else if (temp < 90) {
      System.out.println("It is nice out");
    } else {
      System.out.println("It is hot");
    }

  }

}
